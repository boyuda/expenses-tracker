import mongoose from 'mongoose';

// Creating a Model
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

//Creating a model
const User = mongoose.model('User', userSchema);

export default User;
